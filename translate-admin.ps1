# Apply translations to all admin pages
$adminPages = @(
    'education',
    'experience',
    'projects',
    'skills',
    'hero',
    'gallery',
    'tools'
)

foreach ($page in $adminPages) {
    $filePath = "src\app\admin\$page\page.tsx"
    if (Test-Path $filePath) {
        $content = Get-Content $filePath -Raw -Encoding UTF8
        
        foreach ($bengali in $translations.Keys) {
            $english = $translations[$bengali]
            $content = $content -replace $bengali, $english
        }
        
        Set-Content $filePath $content -NoNewline -Encoding UTF8
        Write-Host "✓ Translated $page/page.tsx"
    }
}

Write-Host "`n✓ All admin pages translated successfully!"
